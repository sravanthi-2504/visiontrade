// SIMPLE categorization for speed
function categorizeStock(stock) {
    const name = stock.name.toUpperCase();

    if (name.includes('BANK')) return name.includes('HOUSING') ? 'Finance' : 'Banking';
    if (name.includes('PHARMA') || name.includes('DRUG')) return 'Pharma';
    if (name.includes('SOFTWARE') || name.includes('TECH')) return 'IT';
    if (name.includes('FINANCE') || name.includes('CAPITAL')) return 'Finance';
    if (name.includes('AUTO') || name.includes('MOTOR')) return 'Automobile';
    if (name.includes('OIL') || name.includes('PETROL')) return 'Oil & Gas';
    if (name.includes('POWER') || name.includes('ENERGY')) return 'Energy';
    if (name.includes('STEEL') || name.includes('METAL')) return 'Metals';
    if (name.includes('CEMENT')) return 'Cement';
    if (name.includes('INSURANCE')) return 'Insurance';

    return 'Other';
}

